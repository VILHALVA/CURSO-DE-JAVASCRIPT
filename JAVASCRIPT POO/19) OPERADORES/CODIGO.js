//Operadores Aritméticos
let a = 5;
let b = 3;
let soma = a + b; // 8
let multiplicacao = a * b; // 15
let resto = a % b; // 2
a++; // Incrementa 'a' para 6
b--; // Decrementa 'b' para 2

//Operadores de Atribuição

